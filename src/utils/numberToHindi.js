function convertBelow1000(n) {
  if (n === 0) return ''
  let result = ''
  if (n >= 100) {
    result += ones[Math.floor(n / 100)] + ' सौ'
    n %= 100
    if (n > 0) result += ' '
  }
  if (n === 0) return result
  if (n < 20) {
    result += ones[n]
  } else if (tensSpecial[n]) {
    result += tensSpecial[n]
  } else {
    result += tensMap[Math.floor(n / 10) * 10]
    if (n % 10 > 0) result += ' ' + ones[n % 10]
  }
  return result
}

export function numberToHindiWords(num) {
  if (isNaN(num) || num === null || num === undefined) return ''
  num = Math.round(num)
  if (num === 0) return 'शून्य रुपये'

  let result = ''
  let n = num

  if (n >= 10000000) {
    result += convertBelow1000(Math.floor(n / 10000000)) + ' करोड़ '
    n %= 10000000
  }
  if (n >= 100000) {
    result += convertBelow1000(Math.floor(n / 100000)) + ' लाख '
    n %= 100000
  }
  if (n >= 1000) {
    result += convertBelow1000(Math.floor(n / 1000)) + ' हजार '
    n %= 1000
  }
  if (n > 0) {
    result += convertBelow1000(n)
  }

  return result.trim() + ' रुपये'
}

export function toHindiNumerals(num) {
  if (num === null || num === undefined) return ''
  const hindiDigits = ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९']
  return String(num).replace(/[0-9]/g, d => hindiDigits[parseInt(d)])
}
